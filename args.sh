# prints value of the 1st command line argument
echo "Value of 0 is"
echo $0 # always represents the 1st CLI arg i.e. the path of the shell script

# prints value of the 2nd command line argument
echo "Value of 1 is"
echo $1

# prints value of the 3rd command line argument
echo "Value of 2 is"
echo $2

# prints value of the 4th command line argument
echo "Value of 3 is"
echo $3